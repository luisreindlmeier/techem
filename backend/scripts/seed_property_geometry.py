def main() -> None:
    if not SUPABASE_URL or not SUPABASE_KEY:
        raise SystemExit('SUPABASE_URL / SUPABASE_SERVICE_ROLE_KEY missing')

    client = create_client(SUPABASE_URL, SUPABASE_KEY)

    print(f'Upserting geometry for {len(PROPERTIES)} properties…')
    for prop in PROPERTIES:
        # Only update the new geometry columns — leave existing columns (e.g.
        # emission_factor_g_kwh) untouched to avoid NOT NULL constraint errors.
        geometry_update = {
            'name':              prop['name'],
            'city':              prop['city'],
            'zipcode':           prop['zipcode'],
            'energysource':      prop['energysource'],
            'lat':               prop['lat'],
            'lng':               prop['lng'],
            'building_height':   prop['building_height'],
            'footprint_polygon': prop['footprint_polygon'],
        }
        if 'street' in prop:
            geometry_update['street'] = prop['street']

        client.table('properties').update(geometry_update).eq('id', prop['id']).execute()
        print(f'  [{prop["id"]:2d}] {prop["name"]}')

    print(f'\nDone — {len(PROPERTIES)} properties seeded.')


if __name__ == '__main__':
    main()
